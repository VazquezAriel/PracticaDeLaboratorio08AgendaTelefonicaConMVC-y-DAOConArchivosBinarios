    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        String cedula = txtCedula.getText();
        Usuario usuario = this.controladorUsuario.buscarUsuario(cedula);
        if (usuario != null) {
            labelTitulo.setText(usuario.getNombre() + " - " + usuario.getApellido());
            txtNombre.setText(usuario.getNombre());
            txtApellido.setText(usuario.getApellido());
            txtCorreo.setText(usuario.getCorreo());
            cargarTelefonosTblTelefonos(controladorUsuario.listarTelefonos(usuario));
            
        } else {
            JOptionPane.showMessageDialog(this, "No se ha encontrado el usuario con la cedula " + cedula);
            limpiar();
        }
        
    }//GEN-LAST:event_botonBuscarActionPerformed

    public void cargarTelefonosTblTelefonos(List<Telefono> telefonos) {
        DefaultTableModel modelo = (DefaultTableModel) tablaTelefonos.getModel();
        modelo.setRowCount(0);
        for (Telefono telefono : telefonos) {
            Object[] rowData = {telefono.getCodigo(), telefono.getTipo(), telefono.getNumero(), telefono.getOperadora()};
            modelo.addRow(rowData);
        }
        tablaTelefonos.setModel(modelo);
    }
