    private void jButtonListarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarTodosActionPerformed

        DefaultTableModel modelo = (DefaultTableModel) tablaTelefonos.getModel();
        limpiar();
        for (Telefono telefono : telefonoDAO.findAll()) {
            Object[] rowData = {telefono.getCodigo(), telefono.getTipo(), telefono.getNumero(), telefono.getOperadora()};
            modelo.addRow(rowData);
        }
        labelTitulo.setText("Usuario");
        tablaTelefonos.setModel(modelo);
    }//GEN-LAST:event_jButtonListarTodosActionPerformed

    public void limpiar() {
        txtApellido.setText("");
        txtCedula.setText("");
        txtCorreo.setText("");
        txtNombre.setText("");
        labelTitulo.setText("Usuario");
        DefaultTableModel modelo = (DefaultTableModel) tablaTelefonos.getModel();
        modelo.setRowCount(0);
    }

